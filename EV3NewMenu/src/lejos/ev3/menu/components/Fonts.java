  public static void main(String[] args) {
    GraphicsLCD canvas = LocalEV3.get().getGraphicsLCD();
    Font font = new Courier13();
    drawAsImage(canvas, font);
    drawAsFont(canvas, font);
  }

  static void drawAsImage(GraphicsLCD canvas, Font font) {
    int w = font.width * font.glyphCount;
    int h = font.height;
    int x = 0;
    while (true) {
      Image img = new Image(w, h, font.glyphs);
      canvas.drawImage(img, 0, 0, 0);
      switch (Button.waitForAnyPress()) {
      case Button.ID_LEFT: {
        x += font.width;
        break;
      }
      case Button.ID_RIGHT: {
        x -= font.width;
        break;
      }
      case Button.ID_ESCAPE:
        return;
      }
      canvas.drawRegion(img, x, 0, w, h, 0, 0, 0, 0);
    }
  }

  static void drawAsFont(GraphicsLCD canvas, Font font) {
    int w = font.width;
    int h = font.height;
    canvas.setFont(font);
    for (int i = font.firstChar; i < font.glyphCount; i++) {
      canvas.drawChar((char) i, (i * w) % 178, ((i * w) / 178) * h, 0);
    }
    Button.waitForAnyPress();
  }

